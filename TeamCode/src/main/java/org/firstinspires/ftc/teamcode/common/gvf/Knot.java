package org.firstinspires.ftc.teamcode.common.gvf;

/** Represents a control point along a 2d path*/
public class Knot {
    public double x, y, heading, splineHeading;

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public double getHeading() {
        return heading;
    }

    public double getSplineHeading() {
        return splineHeading;
    }
}
